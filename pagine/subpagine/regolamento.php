<h2><strong>Regolamento</strong></h2>
<table width="95%" border="0" cellpadding="0" cellspacing="0">
  <tr>
    <td><p><strong>Capitale: </strong></p>
      <p>&nbsp; </p>
      <p>&#149;&nbsp; Il capitale di cui dispone ogni singola squadra &egrave; 250
        Fantamiliardi. </p>
      <p>&#149;&nbsp; Non si pu&ograve; assolutamente andare in rosso, con eventuali
        promesse di rimborso in futuro. </p>
      <p>&nbsp; </p>
      <p><strong>Rose: </strong></p>
      <p>&nbsp; </p>
      <p>&#149;&nbsp; Le rose delle quadre che partecipano al Fantacalcio devono
        essere composte da 25 giocatori acquistati tramite una regolare asta
        fra le squadre partecipanti. </p>
      <p>&#149;&nbsp; I giocatori devono essere cos&igrave; suddivisi: </p>
      <p>&#149;&nbsp; N&deg; 3 Portieri </p>
      <p>&#149;&nbsp; N&deg; 8 Difensori </p>
      <p>&#149;&nbsp; N&deg; 8 Centrocampisti </p>
      <p>&#149;&nbsp; N&deg; 6 Attaccanti </p>
      <p>&nbsp; </p>
      <p><strong>Competizioni: </strong></p>
      <p>&nbsp; </p>
      <p>&#149;&nbsp; Esistono 3 tipi di competizioni: </p>
      <p>&#149;&nbsp; La lega (basato sui 3 punti a partita); </p>
      <p>&#149;&nbsp; Il campionato a punti (basato sulla sommatoria dei punti
        dei giocatori di ogni squadra). </p>
      <p>&#149;&nbsp; La FantaCup (3 punti a partita con gironi all'italiana
        e fase eliminatoria) </p>
      <p>&nbsp; </p>
      <p><strong>Punteggi e classifiche: </strong></p>
      <p>&nbsp; </p>
      <p>&#149;&nbsp; I vari punteggi delle rispettive squadre vengono calcolati
        in base ai voti, ai gol, agli assist, e ai comportamenti disciplinari,
        forniti dalla Gazzetta dello Sport, e vengono cos&igrave; assegnati: </p>
      <p>&#149;&nbsp; 3 punti in pi&ugrave; per ogni gol fatto; </p>
      <p>&#149;&nbsp; 3 punti in pi&ugrave; per ogni rigore parato; </p>
      <p>&#149;&nbsp; 1 punto in pi&ugrave; per ogni assist effettuato; </p>
      <p>&#149;&nbsp; 3 punti in meno per ogni rigore sbagliato; </p>
      <p>&#149;&nbsp; 2 punti in meno per ogni autogol; </p>
      <p>&#149;&nbsp; 1 punto in meno per ogni espulsione; </p>
      <p>&#149;&nbsp; 0,5 punti in meno per ogni ammonizione. </p>
      <p>&nbsp; </p>
      <p>&#149;&nbsp; Il computo dei gol si calcola in base alla sommatoria dei
        voti, opportunamente modificati in base alla regola precedente, degli
        11 giocatori messi in campo dall'allenatore o dalle eventuali riserve.
        Vengono cos&igrave; assegnati: </p>
      <p>&#149;&nbsp; Nessun gol da 0 a 65,5 punti; </p>
      <p>&#149;&nbsp; 1 gol da 66 a 71,5 punti; </p>
      <p>&#149;&nbsp; 2 gol da 72 a 77,5 punti; </p>
      <p>&#149;&nbsp; 3 gol da 78 a 83,5 punti; </p>
      <p>&#149;&nbsp; 4 gol da 84 a 89,5 punti; </p>
      <p>&#149;&nbsp; 5 gol da 90 a 95,5 punti; </p>
      <p>&#149;&nbsp; 6 gol da 96 a 101,5 punti; </p>
      <p>&#149;&nbsp; 7 gol da 102 a 107,5 punti; </p>
      <p>&nbsp; </p>
      <p>&#149;&nbsp; Modificatore difesa: <br>
  Se il portiere e almeno 4 difensori portano punteggio alla squadra si considerano
    i voti del portiere e i 3 voti migliori tra quelli dei difensori. Sulla media
    di questi 4 valori la squadra ottiene: </p>
      <p>&#149;&nbsp;I. +6 punti se &egrave; &gt;= a 7 </p>
      <p>&#149;&nbsp;II. +3 punti se &egrave; &gt;= a 6,5 e &lt; di 7 </p>
      <p>&#149;&nbsp;III. +1 punti se &egrave; &gt;= a 6 e &lt; di 6,5 </p>
      <p>&nbsp; </p>
      <p>&#149;&nbsp; Punteggi e classifiche verranno calcolati automaticamente
        mediante sito web (campionato punti) e semiautomaticamente tramite file
        excel (lega e coppa). </p>
      <p>&nbsp; </p>
      <p>&nbsp; </p>
      <p><strong>Asta iniziale: </strong></p>
      <ul>
        <li>Fase iniziale: si prende posto armati di penna, foglio e quant'altro
          considerato utile. </li>
        <li>Fase centrale: il costo minimo di un giocatore &egrave; rappresentato
          dalla singola unit&agrave; di spesa ovvero 1 Fantamiliardo. Per ogni
          giocatore proposto segue un'asta (non si pu&ograve; offrire di pi&ugrave; del
          capitale rimasto sottratto di 1 per ogni giocatore mancante al completamento
          della rosa di 25 giocatori, pena la fustigazione pubblica e la lapidazione
          al campo di S.Siro o al Delle Alpi o all'Olimpico secondo preferenza
          del “lapidando”) </li>
      </ul>
      <p><em>(Si parte seguendo l'ordine ruolo-allenatore (ogni allenatore al
          suo turno propone un giocatore su cui fare l'asta partendo almeno da
          1). Durante l'asta iniziale non &egrave; possibile svincolare in alcun
          modo un giocatore. Nei casi precedenti invece il giocatore pu&ograve; essere
          svincolato ma a costo 1.) </em></p>
      <p>&#149;&nbsp; Alla fine dell'asta ogni allenatore dovr&agrave; comunicare
        il nome scelto per la propria squadra, la lista dei 25 giocatori e i
        propri colori sociali. Entro un mese dovr&agrave; poi consegnare il proprio
        simbolo sotto forma di immagine (bpm, jpeg, gif, etc…) a chi si occupa
        del sito e del file excel. </p>
      <p>&nbsp; </p>
      <p><strong>Partita: </strong></p>
      <p>&nbsp; </p>
      <p>&#149;&nbsp; Esistono diversi tipi di moduli che l'allenatore pu&ograve; sfruttare
        a seconda delle partite. Questi moduli, che non possono cambiare con
        l'ingresso delle riserve, sono: </p>
      <p>&#149;&nbsp; 4-4-2 </p>
      <p>&#149;&nbsp; 4-3-3 </p>
      <p>&#149;&nbsp; 4-5-1 </p>
      <p>&#149;&nbsp; 5-3-2 </p>
      <p>&#149;&nbsp; 5-4-1 </p>
      <p>&#149;&nbsp; 3-4-3 </p>
      <p>&#149;&nbsp; 3-5-2 </p>
      <p>&#149;&nbsp; Ogni allenatore deve comunicare a tutti gli altri giocatori
        la rosa degli 11 titolari pi&ugrave; le 7 riserve 2 ore prima dell'inizio
        delle partite, sia in caso di anticipi al Sabato, sia in caso di partite
        regolari la Domenica. La comunicazione avviene tramite sito web o nel
        caso di malfunzionamenti di quest'ultimo tramite mail inviate a tutti
        i partecipanti. </p>
      <p>&#149;&nbsp; Chi non consegna la formazione entro questi termini, giocher&agrave; con
        la formazione schierata la settimana precedente. </p>
      <p>&#149;&nbsp; In caso di mancanza di giocatori in un determinato ruolo,
        l'allenatore deve mettere in rosa o in comunque in panchina, a sua discrezione,
        i giocatori non disponibili, con il rischio di giocare la partita in
        minoranza; ogni giocatore mancante, ovviamente porta alla squadra un
        punteggio pari a 0. </p>
      <p>&nbsp; </p>
      <p><strong>Scambio giocatori: </strong></p>
      <p>&nbsp; </p>
      <p>&#149;&nbsp; In qualunque momento del campionato, si possono effettuare
        scambi di giocatori tra le squadre partecipanti. Questo comporta l'azzeramento
        della quotazione del giocatore non appena il passaggio del giocatore &egrave; avvenuto
        (ad es. se Del Piero &egrave; stato acquistato a 40, e viene scambiato,
        il suo valore al momento dello scambio sar&agrave; 1). </p>
      <p>&#149;&nbsp; Le proposte di scambio possono essere effettuate in regolare
        presenza di tutte le squadre partecipanti, oppure solo fra le due squadre
        interessate. In qualunque caso lo scambio deve essere comunicato a tutte
        le squadre entro la mattinata di Sabato per essere giudicato regolare. </p>
      <p>&#149;&nbsp; Gli scambi possono essere effettuati solo dal Luned&igrave; al
        Venerd&igrave;. </p>
      <p>&#149;&nbsp; Non &egrave; possibile effettuare “prestiti” se non per
        una durata di almeno 5 giornate. </p>
      <p>&#149;&nbsp; Sono possibili anche scambi con l'utilizzo di fantamiliardi. </p>
      <p>&#149;&nbsp; Durante gli scambi (come nel mercato) le rose devono mantenersi
        sempre di 25 giocatori. </p>
      <p>&nbsp; </p>
      <p>&nbsp; </p>
      <p>&nbsp; </p>
      <p><strong>FantaMercato: </strong></p>
      <p><strong>&nbsp; </strong></p>
      <p><em>Premi: </em></p>
      <p><em>&nbsp; </em></p>
      <p>&#149;&nbsp; <em>Alla termine di ogni girone della lega ogni squadra
          avr&agrave; in dotazione 10 Fantamiliardi in modo tale da poter effettuare
          il mercato di riparazione, in pi&ugrave; verranno assegnati altri Fantamiliardi
          in base alle posizioni nelle due competizioni principali (un premio
          per ogni competizione): </em></p>
      <p>&#149;&nbsp; <em>In caso di 1&deg; posto saranno assegnati 20 Fantamiliardi; </em></p>
      <p>&#149;&nbsp; <em>In caso di 2&deg; posto saranno assegnati 17 Fantamiliardi; </em></p>
      <p>&#149;&nbsp; <em>In caso di 3&deg; posto saranno assegnati 15 Fantamiliardi; </em></p>
      <p>&#149;&nbsp; <em>In caso di 4&deg;-8&deg; posto saranno assegnati 10
          Fantamiliardi; </em></p>
      <p>&#149;&nbsp; <em>In caso di 9&deg;-11&deg; posto saranno assegnati 5
          Fantamiliardi; </em></p>
      <p><em>&nbsp; </em></p>
      <p>&#149;&nbsp; <em>Per ogni squadra verranno sommati, a seconda delle
          posizioni nei 2 campionati, i Fantamiliardi guadagnati, stabilendo
          cos&igrave; l'effettivo ammontare di ogni squadra. </em></p>
      <p><strong>&nbsp; </strong></p>
      <p>&nbsp; </p>
      <p>&#149;&nbsp; Il FantaMercato avr&agrave; luogo alla conclusione di ogni
        girone e dovranno essere presenti tutte le squadre iscritte al campionato.
        In mancanza anche di una sola squadra il FantaMercato non potr&agrave; avere
        inizio (a meno che l'allenatore della squadra rinunci in maniera esplicita). </p>
      <p>&#149;&nbsp; I primi a partire con il FantaMercato saranno quelli che
        dovranno regolarizzare (25 giocatori) le loro squadre, inserendo quei
        giocatori che man mano durante il campionato sono andati via per diverse
        ragioni e la quotazione di ognuno di questi giocatori sar&agrave; la
        stessa dell'asta iniziale; </p>
      <p>&#149;&nbsp; Ogni FantaAllenatore metter&agrave;, mano a mano durante
        il FantaMercato, i propri giocatori in vendita; possono verificarsi tre
        casi: </p>
      <p>&#149;&nbsp;i. Segue un'asta aperta a tutte le squadre interessate </p>
      <p>&#149;&nbsp;ii. Segue una contrattazione tra singolo acquirente e la
        squadra che detiene il cartellino del giocatore </p>
      <p>&#149;&nbsp;iii. Il giocatore viene svincolato a 1 </p>
      <p>&#149;&nbsp; Nel caso che un giocatore inizialmente svincolato a 1 interessi
        successivamente a pi&ugrave; squadre la base d'asta iniziale del giocatore
        partir&agrave; dalla met&agrave; del cartellino di acquisto. Nel caso
        in cui invece vi sia un singolo acquirente il prezzo del giocatore sar&agrave; fissato
        indiscutibilmente nella met&agrave; del cartellino d'acquisto. In tutti
        i casi il ricavato andr&agrave; alla squadra che ne deteneva il cartellino </p>
      <p>&nbsp; </p>
      <p><strong>LEGA </strong></p>
      <p>&nbsp; </p>
      <p>La Lega &egrave;: </p>
      <p>opzioni: </p>
      <ol>
        <li>a 11 squadre con turno di riposo </li>
        <li>a 12 squadre con “morto” da 1 gol ogni partita </li>
        <li>un partito politico </li>
      </ol>
      <p>&nbsp; </p>
      <p><strong>FANTACUP: </strong></p>
      <p>&nbsp; </p>
      <p>La FantaCup &egrave; una coppa che si disputer&agrave; con gironi all'italiana
        e fase eliminatoria con partite di Andata e Ritorno, finale esclusa. </p>
      <p>Opzioni: con o senza morto nei gironi </p></td>
